    private void jTextField2KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField2KeyTyped
        // TODO add your handling code here:
        if(!jTextField2.getText().equals("")){
        String metin = jTextField2.getText().trim();
        jTextField2.setText(metin.toUpperCase().trim());
        DefaultListModel listmodel = new DefaultListModel();
        listmodel.clear();
        List<codes.urun> urunler = null;
        try {
            urunler = new codes.urun().UrunKoduAra(metin);
        } catch (SQLException ex) {
            Logger.getLogger(Stok_Giris.class.getName()).log(Level.SEVERE, null, ex);
        }
        Vector[] v = new Vector[urunler.size()];
        for(int n=0;n<urunler.size();n++){
            v[n] = new Vector();
            listmodel.add(n, urunler.get(n).getUrun_kodu());
        }
        jList3.setModel(listmodel);
        jList3.repaint();
        }else{
            UrunKoduDetayArama();
        }
       
    }//GEN-LAST:event_jTextField2KeyTyped

