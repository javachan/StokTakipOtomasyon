    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        codes.stok ekle = new codes.stok();
        boolean durum = false;
        String urunKodu = null, tarih ="";
        int adet = 0;
        urunKodu = jList3.getSelectedValue().toString().trim();
        tarih = jFormattedTextField1.getText().trim();
        adet = Integer.parseInt(jTextField1.getText().trim());
        int mId = 0;
        try {
            mId = new codes.urun().MusteriIdBul(jComboBox1.getSelectedItem().toString().trim());
        } catch (SQLException ex) {
            Logger.getLogger(Stok_Giris.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            durum = ekle.StokGiris(urunKodu, tarih, adet,mId);
            if(durum){
                JOptionPane.showMessageDialog(this, "Stok Kaydı Başarılı");
                jFormattedTextField1.setText(dtf.format(localDate));
                jTextField1.setText("");
                jList3.setSelectedIndex(0);
                jList2.setSelectedIndex(0);
                jList1.setSelectedIndex(0);
                jList3.setEnabled(false);
                jList2.setEnabled(false);
                jList1.setEnabled(true);
                
            }
            else{
                JOptionPane.showMessageDialog(this, "Stok Kaydı Başarısız");
                
            }
        } catch (SQLException ex) {
            Logger.getLogger(Stok_Giris.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

