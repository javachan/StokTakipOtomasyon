    private void MusterileriListele(){
        DefaultComboBoxModel combomodel = (DefaultComboBoxModel) jComboBox1.getModel();
        combomodel.removeAllElements();
        combomodel.addElement("Lütfen Bir Şirket Seçiniz");
        List<codes.stok> stok = null;
        try {
            stok = new codes.stok().MusteriListesi();
        } catch (SQLException ex) {
            Logger.getLogger(Stok_Giris.class.getName()).log(Level.SEVERE, null, ex);
        }
        Vector[] v = new Vector[stok.size()];
        for(int n=0;n<stok.size();n++){
            v[n] = new Vector();
            combomodel.addElement(stok.get(n).getMusteri_ad());
        }
        jComboBox1.setModel(combomodel);
        jComboBox1.repaint();
    }
